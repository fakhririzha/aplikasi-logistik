<h3>Layanan</h3>
<p>
	Website ini adalah salah satu langkah yang kami ambil untuk tujuan meningkatkan kepuasan pelanggan kami.
	Dengan website ini pelanggan kami dapat dengan mudah meminta dan melakukan komunikasi dengan kami dalam pelayanan pengiriman
	barang yang diminta. Tentunya kami akan menyediakan beberapa tawaran dan kelebihan yang dapat pelanggan dapatkan.
</p>
<p>
	Keuntungan menggunakan website ini dalam layanan yang kami berikan, antara lain sebagai berikut:
	<ul>
		<li>Mendapatkan informasi-informasi promo yang Ada.</li>
		<li>Pendaftaran pelanggan yang mudah dan interaktif.</li>
		<li>Mempermudah pelanggan dalam melihat informasi biaya pengiriman.</li>
		<li>Pelanggan dapat melakukan <em>tracking</em> pengiriman barang.</li>
		<li>Dan masih banyak lagi.</li>
	</ul>
</p>
<p>
	Dengan berbagai kemudahan dan layanan yang kami tawarkan tersebut, kami siap dalam melayani pelanggan kami semaksimal mungkin
	untuk memenuhi kepuasan pelanggan.
</p>