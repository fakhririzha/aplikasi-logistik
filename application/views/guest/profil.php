<h3>Apa itu PesanTruk?</h3>
<p>
	Pesan Truk adalah platform layanan logistik berbasis aplikasi web yang menghubungkan antara pengirim barang (shipper) dan pengantar barang (transporter) melalui proses pengiriman yang bisa dimonitor secara langsung dan menggunakan sistem pelaporan yang detail.
	<br><br>
	Melalui aplikasi Pesan Truk Anda dapat memperluas dan mengembangkan potensi bisnis perusahaan anda. Dengan menggunakan platform dan teknologi yang kami sediakan, Anda dapat mengoptimalkan pengiriman serta menghemat waktu dan biaya pengiriman, Anda juga dapat mengakses informasi lokasi barang yang Anda kirimkan.
</p>
