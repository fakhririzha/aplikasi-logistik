function win_popup(URL, width, height) {
	window.open(URL, 'PT. Haluan Indah Transporindo', 
		'status=no, toolbar=no, location=no, menubar=no, directories=no, resizable=no, scrollbars=yes, width='+width+', height='+height+'');
}

function win_close() {
	window.close();
}

function win_back() {
	window.back();
}